class Solution {
    public String removeOuterParentheses(String s) {

        StringBuilder sb = new StringBuilder();

        int left = 0;
        int right = 0;

        int index = 0;

        for (char c : s.toCharArray()) {
            if (c == '(') {
                left++;
            } else {
                right++;
            }
            if (left == right) {
                sb.append(s, index+1, index + left + right-1);//ç›´æ¥æŠŠæœ€å¤–å±‚æ‹¬å·åˆ æ‰
                index = index + left + right;
                left = 0;
                right = 0;//é‡æ–°è®¡æ•°
            }
        }
        return sb.toString();

    }
}
